export class OpenWeatherMap {
  private static readonly SOURCE_URL = 'https://api.openweathermap.org/data/3.0/onecall/';

  #apiKey: string;

  public location: OWMLocation;

  constructor(apiKey: string, location: OWMLocation) {
    this.#apiKey = apiKey;
    this.location = location;
  }

  public async fetch({ exclude = ['minutely'], units, lang }: OpenWeatherMapProps): Promise<OWMResponse> {
    const { lat, lon } = this.location;

    const url = new URL(OpenWeatherMap.SOURCE_URL);
    url.searchParams.set('appid', this.#apiKey);
    url.searchParams.set('lat', lat.toFixed(4));
    url.searchParams.set('lon', lon.toFixed(4));

    if (units) url.searchParams.set('units', units);
    if (lang) url.searchParams.set('lang', lang);
    if (exclude) url.searchParams.set('exclude', exclude.join(','));

    return FAKE_DATA;
  }
}

